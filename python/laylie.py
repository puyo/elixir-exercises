#!/usr/bin/env python3

name = input('What is your name? ')

if name.lower() == 'gregory':
    print('Greg is a poo.')

elif name.lower() == 'greg':
    print('Greg is the pooiest poo.')

elif name.lower() == 'eulalie':
    print('You are the best, Eulalie!')

elif name.lower() == 'ruby':
    print('You are always right about everything no sarcasm.')

else:
    print("I don't know you.")

